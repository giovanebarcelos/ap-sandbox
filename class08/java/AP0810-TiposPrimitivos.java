public class TiposPrimitivos {
    public static void main(String[] args) {
        int inteiro = 42;
        double real = 3.14159;
        char caractere = 'A';
        boolean logico = true;
        System.out.println("Inteiro (int): " + inteiro);
        System.out.println("Real (double): " + real);
        System.out.println("Caractere (char): " + caractere);
        System.out.println("Lógico (boolean): " + logico);
    }
}